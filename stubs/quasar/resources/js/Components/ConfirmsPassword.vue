<script setup>
import { ref, reactive, nextTick } from "vue";

const emit = defineEmits(["confirmed"]);

defineProps({
    title: {
        type: String,
        default: "Confirm Password",
    },
    content: {
        type: String,
        default: "For your security, please confirm your password to continue.",
    },
    button: {
        type: String,
        default: "Confirm",
    },
});

const confirmingPassword = ref(false);

const form = reactive({
    password: "",
    error: "",
    processing: false,
});

const passwordInput = ref(null);

const startConfirmingPassword = () => {
    axios.get(route("password.confirmation")).then((response) => {
        if (response.data.confirmed) {
            emit("confirmed");
        } else {
            confirmingPassword.value = true;
            setTimeout(() => passwordInput.value.focus(), 250);
        }
    });
};

const confirmPassword = () => {
    form.processing = true;

    axios
        .post(route("password.confirm"), {
            password: form.password,
        })
        .then(() => {
            form.processing = false;

            closeModal();
            nextTick().then(() => emit("confirmed"));
        })
        .catch((error) => {
            form.processing = false;
            form.error = error.response.data.errors.password[0];
            passwordInput.value.focus();
        });
};

const closeModal = () => {
    confirmingPassword.value = false;
    form.password = "";
    form.error = "";
};
</script>

<template>
    <span>
        <span @click="startConfirmingPassword">
            <slot />
        </span>

        <q-dialog v-model="confirmingPassword" persistent>
            <q-card>
                <q-card-section class="row items-center">
                    <q-avatar
                        icon="password"
                        color="primary"
                        text-color="white"
                    />
                    <span class="q-ml-sm">{{ $t("g.confirm_password") }}</span>
                </q-card-section>
                <q-card-section class="row items-center">
                    <q-input
                        ref="passwordInput"
                        filled
                        clearable
                        v-model="form.password"
                        :label="$t('g.login_password')"
                        lazy-rules
                        :rules="[(val) => !!val || $t('v.required')]"
                        type="password"
                        :error-message="form.error"
                        :error="form.error ? true : false"
                        @keyup.enter="confirmPassword"
                    />
                </q-card-section>
                <q-card-actions align="right">
                    <q-btn flat :label="$t('g.cancel')" color="primary" v-close-popup />
                    <q-btn
                        flat
                        :label="$t('g.confirm_password')"
                        color="primary"

                        :disabled="form.processing"
                        @click="confirmPassword"
                    />
                </q-card-actions>
            </q-card>
        </q-dialog>

